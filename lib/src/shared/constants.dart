class Alerts {
  static void showError({required String message}) {
    Get.snackbar(
      'Error',
      message,
      snackPosition: SnackPosition.BOTTOM,
      backgroundColor: ColorsPallet.red500,
      colorText: Colors.white,
      icon: const Icon(
        Icons.error,
        color: Colors.white,
      ),
    );
  }

  static void showSuccess({required String message}) {
    Get.snackbar(
      'Sucesso!',
      message,
      snackPosition: SnackPosition.BOTTOM,
      backgroundColor: ColorsPallet.green500,
      colorText: Colors.white,
      icon: const Icon(
        Icons.check_circle,
        color: Colors.white,
      ),
    );
  }

  static void showInfo({required String message}) {
    Get.snackbar(
      'Info',
      message,
      snackPosition: SnackPosition.BOTTOM,
      backgroundColor: ColorsPallet.blue500,
      colorText: Colors.white,
      icon: const Icon(
        Icons.info,
        color: Colors.white,
      ),
    );
  }
}

